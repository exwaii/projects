def caesar(de,code,shift):
    if de!="encode":
        shift=-shift
    code=[code[i] for i in range(len(code))]
    ncode=[]
    for i in range(len(code)):
        if code[i] in alphabet:
            ncode.append(alphabet[(alphabet.index(code[i])+shift)%26])
        else:
            ncode.append(code[i])
    return "".join(ncode)    
